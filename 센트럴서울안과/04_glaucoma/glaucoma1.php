				<strong>WORLD CLASS CAREER</strong><br/>
				센트럴서울안과 녹내장 분과 책임전문의인 최재완 원장은 안과의사로서 최고의 커리어를 쌓아 오고 있습니다. 최재완 원장은 서울의대를 졸업하고, 서울아산병원에서 수련의, 안과 전문의, 녹내장 임상강사 과정을 수료하였습니다. 미국 예일대와 캐나다 몬트리올대에 방문교수 경력을 쌓았고, 이후 안과전문병원인 한길안과병원에서 약 5년간의 임상경험을 쌓았습니다. 의학박사 학위는 서울아산병원(울산의대)에서 수여 받았습니다. 최재완 원장은 2019년까지 45편 이상의 녹내장 관련 학술논문들에 주요저자 혹은 공저자로 참여하였습니다. 세계안과학회 4회, 아시아태평양안과학회 3회, 세계녹내장학회 강의 2회 등 세계 최고 수준의 안과학회들의 주요 초청연자로 활동해 오고 있습니다. 세계안과학회에서는 2016년, 2018년 연속으로 최우수학술상을 수상하였습니다. 미국시과학회지 Investigative Ophthalmology and Visual Science, 미국안과학회지 Ophthalmology, 영국안과학회지 British Journal of Ophthalmology 등 유수의 학술지의 심사위원을 역임하였으며, 2018년에는 미국안과학회지의 ‘뛰어난 심사위원상 (Outstanding Reviewer)’을 수상하였습니다. 또한, 세계 유수의 다국적 안과전문기업들의 의료 자문역을 맡아 오고 있습니다.<br/><br/>
				 

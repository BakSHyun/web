				센트럴서울안과 백내장굴절수술 클리닉은 의료진 누적 백내장 수술 건수 10,000례 이상, 실명 등과 관련한 중대 수술 합병증 0%의 탁월한 임상 성적으로, 3년 연속으로 백내장 수술 부문 한국소비자만족지수 1위를 수상하는 등 여러 공신력 있는 기관들과 의료소비자들의 검증을 받았습니다. 또한 다른 개원가의 병원들과는 달리 백내장 수술 이외에도 노년기에 발생할 수 있는 녹내장, 황반변성 같은 퇴행성 안과질환 치료 체계도 충실히 갖추고 있어 노령화 시대 안질환 치료에 가장 적합한 ‘바른’ 진료 체계를 갖추고 있다고 자부합니다. <br/><br/>  </p>

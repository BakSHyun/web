				<h1>클리닉 소개</h1>
				<p>백내장 수술은 국내에서도 연간 200,000건 이상 이루어지고 있는 대표적인 안과 수술입니다. 백내장은 눈 안의 렌즈 역할을 하는 수정체가 혼탁해 지면서 시력이 떨어지는 질환입니다. 백내장 수술은 비교적 간단한 수술로 알려져 있지만, 사실은 매우 복잡한 과정을 거쳐 이루어집니다. 과거의 백내장 수술이 단순 백내장 제거 및 인공수정체 삽입에 국한되었다면, 최근에는 초정밀 진단장비들로 눈 상태를 계측하고, 환자 개개인이 원하는 바를 상담하여 맞춤형으로 굴절이상까지 교정하는 ‘백내장굴절수술 (Cataract Refractive Surgery)’의 패러다임으로 발전하였습니다. 특히 최근에는 ‘섬세한 시력의 질 (Fine Quality of Vision)’을 보장할 수 있는 방법들이 개발되면서, 백내장 수술이 역량을 갖춘 전문클리닉으로 집중되는 현상이 더욱 뚜렷해지고 있습니다. 왜냐하면, 레이저 안구계측기, 최고 사양의 수술 현미경, 컴퓨터 네비게이션 장비 등 소규모 병원에서는 갖추기 어려운 고가의 의료장비들과 경험이 풍부한 의료진을 갖춘 전문 클리닉에서는  다른 병원보다 더 나은 결과를 만들어 내는 것이 가능해졌기 때문입니다. 수술 결과는 데이터베이스화하여 학계에 보고하고, 결과를 다시 피드백하여 ‘노모그램 (Normogram)’을 작성하여 임상진료에 반영됩니다. <br/><br/>센트럴서울안과 백내장굴절수술 클리닉은 2011년 개원 당시 세계유일의 백내장-망막 수술장비이던 콘스텔레이션 시스템 도입으로 안과학계에서도 큰 화제가 되었으며, 이후 난시교정이 가능한 노안교정렌즈 (아트렌즈) 도입, 중간거리용  노안교정렌즈 (렌티스 컴포트) 도입 등 대한민국의 백내장굴절수술의 지평을 세계로 넓혀 가는데 기여해 왔습니다. 2015년 리노베이션에서는 독일 칼 자이스사의 컴퓨터 네비게이션 장비인 ‘칼리스토 아이 CALLISTO Eye’와 세계 최고의 수술현미경 ‘루메라 Lumera 700’을 기반으로 한 ‘자이스 백내장 수술 스위트/ 마커리스  Zeiss Cataract Suite/ MARKERLESS’ 시스템을 도입하여 명실상부한 세계 톱 클래스의 백내장 수술 클리닉으로 도약을 시도 중입니다. <br/><br/>

